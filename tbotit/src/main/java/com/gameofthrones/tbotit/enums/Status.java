package com.gameofthrones.tbotit.enums;

public enum Status {

    ALIVE,
    DEAD,
    MISSING,
    CAPTURED,
    INJURED,
    BETRAYED,
    IMPRISONED,
    EXILED,
    RAPED,
    POISONED,
    ON_THE_RUN,
    KING_OR_QUEEN,
    DETHRONED,
    UNDER_SIEGE,
    EXECUTED,
    RESURRECTED,
    MARRIED,
    DISGUISED,
    ENSLAVED,
    TRAITOR
}

//ALIVE – Personagem está vivo.
//DEAD – Personagem morreu.
//MISSING – Personagem está desaparecido.
//CAPTURED – Personagem foi capturado.
//INJURED – Personagem está ferido.
//BETRAYED – Personagem foi traído por aliados ou amigos.
//IMPRISONED – Personagem está preso em uma masmorra ou cela.
//EXILED – Personagem foi exilado de seu reino ou lar.
//RAPED – Personagem sofreu abuso sexual.
//POISONED – Personagem foi envenenado.
//ON_THE_RUN – Personagem está fugindo.
//KING_OR_QUEEN – Personagem se tornou rei ou rainha.
//DETHRONED – Personagem perdeu o trono.
//UNDER_SIEGE – Personagem está cercado por inimigos em uma fortaleza.
//EXECUTED – Personagem foi executado.
//RESURRECTED – Personagem foi ressuscitado.
//MARRIED – Personagem está casado.
//DISGUISED – Personagem está disfarçado, escondendo sua verdadeira identidade.
//ENSLAVED – Personagem foi escravizado.
//TRAITOR – Personagem se tornou um traidor de sua casa ou aliados.
